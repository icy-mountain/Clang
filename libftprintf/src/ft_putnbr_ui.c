#include "libftprintf.h"

void	ft_putnbr_ui(unsigned int nb)
{
	char	ans;

	if (nb < 10)
	{
		ans = nb + '0';
		write(1, &ans, 1);
	}
	else
	{
		ft_putnbr_ui(nb / 10);
		ans = nb % 10 + '0';
		write(1, &ans, 1);
	}
}
