#include "doop.h"

int		plus(int l, int r)
{
	return (l + r);
}

int		minus(int l, int r)
{
	return (l - r);
}

int		mul(int l, int r)
{
	return (l * r);
}

int		div(int l, int r)
{
	return (l / r);
}

int		mod(int l, int r)
{
	return (l % r);
}
