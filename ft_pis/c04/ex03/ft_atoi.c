#include <unistd.h>

int		ft_atoi(char *str)
{
	int		np;
	int		ans;

	np = 1;
	while (*str != '\0')
	{
		if (*str == '-')
			np *= -1;
		if ('0' <= *str && *str <= '9')
		{
			if (*str == '0')
				return (0);
			ans = *str - '0';
			while (1 && str++)
			{
				if ('0' <= *str && *str <= '9')
					ans = *str == '0' ? ans * 10 : ans * 10 + *str - '0';
				else
					return (np * ans);
			}
		}
		str++;
	}
	return (0);
}
