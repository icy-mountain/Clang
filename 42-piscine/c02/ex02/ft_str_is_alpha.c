#include <unistd.h>

int		ft_str_is_alpha(char *str)
{
	int		ans;

	ans = 1;
	while (*str != '\0')
	{
		if ('A' <= *str && *str <= 'Z')
			ans = 1;
		else if ('a' <= *str && *str <= 'z')
			ans = 1;
		else
			return (0);
		str++;
	}
	return (ans);
}
