#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*p;

	p = str;
	while (*str != '\0')
	{
		if ('A' <= *str && *str <= 'Z')
			*str += 32;
		str++;
	}
	return (p);
}
