#include <unistd.h>

char	*ft_strupcase(char *str)
{
	char	*p;

	p = str;
	while (*str != '\0')
	{
		if ('a' <= *str && *str <= 'z')
			*str -= 32;
		str++;
	}
	return (p);
}
