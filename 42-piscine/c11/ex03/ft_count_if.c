int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int		ans;
	int		idx;

	idx = 0;
	ans = 0;
	while (idx < length)
	{
		if (f(tab[idx]) != 0)
			ans++;
		idx++;
	}
	return (ans);
}
