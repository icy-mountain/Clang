#include <unistd.h>

int		ft_strcmp(char *s1, char *s2)
{
	while (1)
	{
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
		if (*s1 != *s2 || *s1 == '\0' || *s2 == '\0')
			return (*s1 - *s2);
		s1++;
		s2++;
	}
}
