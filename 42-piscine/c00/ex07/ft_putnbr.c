#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	ans;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-1 * nb);
	}
	else if (nb < 10)
	{
		ans = nb + '0';
		write(1, &ans, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		ans = nb % 10 + '0';
		write(1, &ans, 1);
	}
}
